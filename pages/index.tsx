import type { NextPage } from 'next'
import { useState, useCallback } from 'react'
import Head from 'next/head'
import styles from '../styles/Home.module.css'
import Layout from './components/layout'
import { Col, Row, Container, Button, Input } from 'reactstrap'

const Home: NextPage = () => {
    // window.open(`https://shasta.tronscan.org/#/transaction/${res}`);

    const [walletAddress, setWalletAddress] = useState('')
    const [response, setResponse] = useState({
        amount: '',
        address: '',
        tx: '',
    })

    const handleChange = useCallback(async () => {
        const res = await fetch(`/api/address/${walletAddress}`)
        const { amount, address, tx } = await res.json()
        setResponse({ amount, address, tx })
        console.log(tx)
        window.open(`https://shasta.tronscan.org/#/transaction/${tx}`)
    }, [walletAddress])

    return (
        <Layout>
            <div className={styles.container}>
                <Head>
                    <title>Create Next App</title>
                    <meta name="description" content="Generated by create next app" />
                    <link rel="icon" href="/favicon.ico" />
                </Head>

                {/* Title */}
                <Container fluid="true" className={styles.title}>
                    <h1>TRON FAUCET</h1>
                    <h4>By El Dorado Io</h4>
                </Container>

                <br />

                {/* Wallet Info */}
                <Container fluid="true" className={styles.transaction}>
                    <Row fluid="true" xs="12">
                        <Col fluid="true" xs="10" className={styles.input}>
                            <Input
                                className={styles.input}
                                type="text"
                                placeholder="Enter wallet address"
                                value={walletAddress}
                                onChange={e => setWalletAddress(e.target.value)}
                            />
                        </Col>
                        <Col fluid="true" xs="2" className={styles.button}>
                            <Button className={styles.button} onClick={handleChange}>Enviar</Button>
                        </Col>
                    </Row>
                    <hr/>
                    <Row fluid="true" xs="12">
                        <h2>Tx: {response.tx}</h2>
                        <h2>Amount: {response.amount}</h2>
                        <h2>Address: {response.address}</h2>
                    </Row>
                </Container>
                
                {/* <input
                    type="text"
                    placeholder="Enter wallet address"
                    value={walletAddress}
                    onChange={e => setWalletAddress(e.target.value)}
                />
                <button style={{ marginTop: 10 }} onClick={handleChange}>
                    Enviar
                </button> */}
            </div>
        </Layout>
    )
}

export default Home
